public class product {
        String name, date;
        int id, price;
        public product(String name, String date, int id, int price) {
            this.name = name;
            this.date = date;
            this.id = id;
            this.price=price;
        }
        public String getName() {
            return name;
        }
        public int getPrice() {
            return price;
        }
        
        
}
